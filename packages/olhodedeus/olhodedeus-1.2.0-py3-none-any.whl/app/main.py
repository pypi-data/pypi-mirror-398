def carregar_modulo_dinamico(nome_modulo: str, caminho: str):
    """Carrega um m├│dulo Python dinamicamente."""
    import importlib.util
    
    if not os.path.exists(caminho):
        print(f"M├│dulo n├Бo encontrado: {caminho}")
        return None
    
    try:
        spec = importlib.util.spec_from_file_location(nome_modulo, caminho)
        module = importlib.util.module_from_spec(spec)
        spec.loader.exec_module(module)
        return module
    except Exception as e:
        print(f"Erro ao carregar m├│dulo {nome_modulo}: {e}")
        return None


