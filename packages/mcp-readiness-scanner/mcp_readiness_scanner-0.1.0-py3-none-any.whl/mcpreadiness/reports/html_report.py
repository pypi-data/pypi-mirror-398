def render_html(result: ScanResult, verbose: bool = False) -> str:
    """
    Render a scan result as HTML.

    Args:
        result: The scan result to render
        verbose: Include detailed information

    Returns:
        Self-contained HTML string with embedded CSS
    """
    # Build HTML parts
    head = _render_head(result.target)
    summary = _render_summary(result)
    findings = _render_findings(result, verbose)

    return f"""<!DOCTYPE html>
<html lang="en">
{head}
<body>
    <div class="container">
        <header>
            <h1>üîç MCP Readiness Scan Report</h1>
            <p class="subtitle">Production readiness analysis</p>
        </header>

        {summary}
        {findings}

        <footer>
            <p>Generated by <a href="https://github.com/mcp-readiness/scanner" target="_blank">MCP Readiness Scanner</a> on {datetime.utcnow().strftime("%Y-%m-%d %H:%M:%S")} UTC</p>
        </footer>
    </div>

    <script>
        // Toggle finding details
        function toggleDetails(id) {{
            const details = document.getElementById('details-' + id);
            const toggle = document.getElementById('toggle-' + id);
            if (details.style.display === 'none') {{
                details.style.display = 'block';
                toggle.textContent = '‚ñº';
            }} else {{
                details.style.display = 'none';
                toggle.textContent = '‚ñ∂';
            }}
        }}

        // Filter by severity
        function filterBySeverity(severity) {{
            const findings = document.querySelectorAll('.finding-item');
            findings.forEach(finding => {{
                if (severity === 'all' || finding.dataset.severity === severity) {{
                    finding.style.display = 'block';
                }} else {{
                    finding.style.display = 'none';
                }}
            }});

            // Update active filter button
            document.querySelectorAll('.filter-btn').forEach(btn => {{
                btn.classList.remove('active');
            }});
            event.target.classList.add('active');
        }}
    </script>
</body>
</html>"""


